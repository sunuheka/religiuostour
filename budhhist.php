                <p> According to the Mahaparinirvana Sutra (the sutra of the Great Demise), in the last discourse of the Buddha (6th BCE), he spoke about the sacredness of certain places associated with his life. These were: 
                    <ul>
                        <li><b>Lumbini:</b> the place where he was born; </li>
                        <li><b>Bodhgaya:</b> the place where he attained Enlightenment;</li>
                        <li><b>Sarnath:</b> the place where he preached the first sermon; </li> 
                        <li><b>Kushinagara:</b> the place where he attained Mahaparinirvana. </li></ul></p>
 
<p>The Buddha emphasized that devotees should visit and venerate these holy places as a way of purifying their thought, speech and action. In other words, pilgrimage to the holy places would energise the devotee to make progress on following the Buddha’s Eightfold Path. It is widely believed today that pilgrimage to these holy places with the correct attitude can help devotees advance on the path of the Dhamma (Dharma).</p>
<p> 
In the 3rd century BCE, roughly two centuries after the demise (Mahaparinirvana) of the Buddha, the Indian Subcontinent came under the rule of Emperor Ashoka of the Mauryan Dynasty. After having fought many bloody battles, Ashoka embraced the teachings of Buddha (Dhamma). He dedicated the rest of his life to spreading Dhamma. He erected rock edicts bearing inscriptions on Dhamma, sent missions to far-off lands to disseminate Dhamma, and undertook Dhamma pilgrimage to the four sacred places mentioned in <a href="">Mahaparinirvana Sutra</a>  and the four places where Buddha performed miracles. These four places where Buddha performed miracles were: 
    <ul><li><b>Rajagriha: </b>the place where the Buddha tamed the elephant Nalagiri;</li>
    <li><b>Vaishali: </b>the place where monkey offered honey to the Buddha;</li>
    <li><b>Shravasti: </b>the place where the Buddha performed the miracle of pairs; and</li>
    <li><b>Sankisa: </b>the place where Buddha descended from tushita  heaven. </li></ul>
    <p>The four sacred places and the four places of miracles  are together known as <a href="">Atthamahathanani</a> or the Eight Great Places. Pilgrimage to these eight shrines is known as “dhamma yatra” (Dhamma expedition) or a pilgrimage of piety. It used to be the goal of each Buddhist to visit these sites in their lifetime.</p>
                
